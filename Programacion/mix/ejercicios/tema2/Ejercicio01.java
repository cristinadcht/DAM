package com.ejercicios.tema2;

public class Ejercicio01 {
	/*
	* Ejercicio 1: Declaración e inicialización de variables
	* 1. Declara una variable int llamada edad y asígnale el valor 25.
	* 2. Declara una variable double llamada altura y asígnale el valor 1.75.
	* 3. Declara una variable boolean llamada esEstudiante y asígnale el valor true.
	* 4. Declara una variable char llamada inicial y asígnale la letra 'A'.
	* 5. Declara una variable String llamada nombre y asígnale el valor "Carlos".
	* 6. Imprime cada una de las variables en la consola.
	*/
	public static void main(String[]args) {
		int edad = 25;
		System.out.println(edad);
		double altura = 1.75;
		System.out.println(altura);
		boolean esEstudiante = true;
		System.out.println(esEstudiante);
		char inicial = 'A';
		System.out.println(inicial);
		String nombre = "Carlos";
		System.out.println(nombre);
	}
}
